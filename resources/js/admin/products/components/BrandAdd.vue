<template>
  <div class="component-wrap">
    <v-card>
      <v-card-title>Test</v-card-title>
      <v-form v-model="valid" ref="brandFormAdd" lazy-validation>
        <v-container grid-list-md>
          <v-layout row wrap>
            <v-flex xs12>
              <div class="body-2 white--text">Brand Details</div>
            </v-flex>
            <v-flex xs12>
              <v-text-field label="Name" v-model="name" :rules="nameRules"></v-text-field>
            </v-flex>
            <v-flex xs12>
              <v-btn @click="save()" :disabled="!valid" color="primary">Save</v-btn>
            </v-flex>
          </v-layout>
        </v-container>
      </v-form>
    </v-card>
  </div>
</template>

<script>
export default {
  data() {
    return {
      valid: false,
      isLoading: false,
      name: "",
      nameRules: [v => !!v || "Name is required"],
    };
  },
  mounted() {
    console.log("pages.products.components.BrandAdd.vue");

    const self = this;
  },
  methods: {
    save() {
      const self = this;

      let payload = {
        name: self.name,
      };

      self.isLoading = true;

      axios
        .post("/admin/brand", payload)
        .then(function(response) {
          self.$store.commit("showSnackbar", {
            message: response.data.message,
            color: "success",
            duration: 3000
          });

          self.isLoading = false;
          self.$eventBus.$emit("BRAND_ADDED");

          // reset
          self.$refs.brandFormAdd.reset();
        })
        .catch(function(error) {
          self.isLoading = false;
          if (error.response) {
            self.$store.commit("showSnackbar", {
              message: error.response.data.message,
              color: "error",
              duration: 3000
            });
          } else if (error.request) {
            console.log(error.request);
          } else {
            console.log("Error", error.message);
          }
        });
    }
  }
};
</script>
